function generateSVG(bg, pixelData) {
	let rects = '';
	for (const [color, coords] of Object.entries(pixelData)) {
		for (const [x, y] of coords) {
			rects += `<rect x="${x}" y="${y}" width="1" height="1" fill="${color}"/>`;
		}
	}
	return `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 16 16" width="128" height="128" shape-rendering="crispEdges">
<rect width="16" height="16" fill="${bg}" rx="3"/>
${rects}
</svg>`;
}

