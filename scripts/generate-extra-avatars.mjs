function main() {
	fs.mkdirSync(AVATARS_DIR, { recursive: true });

	for (const avatar of avatars) {
		const filepath = path.join(AVATARS_DIR, `avatar-${avatar.id}.svg`);

		// Safety: do NOT overwrite existing avatars 1-10
		if (avatar.id <= 10) {
			console.log(`Skipping avatar-${avatar.id}.svg (already exists)`);
			continue;
		}

		const svg = generateSVG(avatar.bg, avatar.pixels);
		fs.writeFileSync(filepath, svg);
		console.log(`Generated: avatar-${avatar.id}.svg (${avatar.name})`);
	}

	console.log(`\nDone! Generated ${avatars.length} extra avatars (IDs 11-34).`);
}

main();
